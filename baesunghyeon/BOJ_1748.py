import sys
N = int(sys.stdin.readline())

if N < 10:  # 10보다 작은경우
    print(N)
elif N < 100:  # 100 보다 작은경우 / 10 일때는 + 1
    if N == 10:
        print(9 + (N - 10) * 2 + 1)  # 1의자리 9개 / 10의자리 갯수 * 2 / 10일때만 + 1
    else:
        print(9 + (N - 10) * 2 + 2)
elif N < 1000:  # 1000 보다 작은경우 / 100 때문에 + 1
    if N == 100:
        print(9 + 90 * 2 + (N - 100) * 3 + 1)
    else:
        print(9 + 90 * 2 + (N - 100) * 3 + 3)
elif N < 10000:
    if N == 1000:
        print(9 + 90 * 2 + 900 * 3 + (N - 1000) * 4 + 1)
    else:
        print(9 + 90 * 2 + 900 * 3 + (N - 1000) * 4 + 4)
elif N < 100000:
    if N == 10000:
        print(9 + 90 * 2 + 900 * 3 + 9000 * 4 + (N - 10000) * 5 + 1)
    else:
        print(9 + 90 * 2 + 900 * 3 + 9000 * 4 + (N - 10000) * 5 + 5)
elif N < 1000000:
    if N == 100000:
        print(9 + 90 * 2 + 900 * 3 + 9000 * 4 + 90000 * 5 + (N - 100000) * 6 + 1)
    else:
        print(9 + 90 * 2 + 900 * 3 + 9000 * 4 + 90000 * 5 + (N - 100000) * 6 + 6)
elif N < 10000000:
    if N == 1000000:
        print(9 + 90 * 2 + 900 * 3 + 9000 * 4 + 90000 * 5 + 900000 * 6 + (N - 1000000) * 7 + 1)
    else:
        print(9 + 90 * 2 + 900 * 3 + 9000 * 4 + 90000 * 5 + 900000 * 6 + (N - 1000000) * 7 + 7)
elif N < 100000000:
    if N == 10000000:
        print(9 + 90 * 2 + 900 * 3 + 9000 * 4 + 90000 * 5 + 900000 * 6 + 9000000 * 7 + (N - 10000000) * 8 + 1)
    else:
        print(9 + 90 * 2 + 900 * 3 + 9000 * 4 + 90000 * 5 + 900000 * 6 + 9000000 * 7 + (N - 10000000) * 8 + 8)
elif N == 100000000:
    print(9 + 90 * 2 + 900 * 3 + 9000 * 4 + 90000 * 5 + 900000 * 6 + 9000000 * 7 + (N - 10000000) * 8 + 8 + 1)

